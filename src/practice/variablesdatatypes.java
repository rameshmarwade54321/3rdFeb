package practice;

public class variablesdatatypes {
	public static void main(String[] args) {
		System.out.println("String datatype");
		String Name;
		Name = "Ramesh";
		System.out.println(Name);
		String Lname = "Marwade";
		System.out.println(Lname);
		System.out.println("int datatype");
		int number;
		number = 55;
		System.out.println(number);
		int number1 = 65;
		System.out.println(number1);
		System.out.println("float datatype");
		float num1;
		num1 = 0.1254f;
		System.out.println(num1);
		float num2 = 50.26f;
		System.out.println(num2);
		System.out.println("char datatype ");
		char grade;
		grade = 'A';
		System.out.println(grade);
		char grade1 = 'B';
		System.out.println(grade1);
		System.out.println("byte datatype");
		byte N1;
		N1 = -125;
		System.out.println(N1);
		byte N2 = 120;
		System.out.println(N2);
		System.out.println("short datatype");
		short Digit;
		Digit = 12556;
		System.out.println(Digit);
		short D1 = 12445;
		System.out.println(D1);
		System.out.println("long datatype");
		long Num3;
		Num3 = 1346455555554444444l;
		System.out.println(Num3);
		long num4 = 1452145215525525554l;
		System.out.println(num4);
		System.out.println("double datatype");
		double Num5;
		Num5 = 123445650.12355d;
		System.out.println(Num5);
		double Num6 = 12345679.231555d;
		System.out.println(Num6);
	}
}

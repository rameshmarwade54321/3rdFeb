package methods_with_parameters;
              // area of rectangle using passing parameter
               //area of circle using passing parameter
public class demo7 {
public static void main(String[] args) {
	areaofrectangle(3, 5);
	areaofcircle(5);
}
public static void areaofrectangle(int length, int width) {
	System.out.println(length*width);
}

public static void areaofcircle(int r) {
	
	System.out.println(Math.PI*r*r);
}





}
